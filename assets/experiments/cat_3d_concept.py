#!/usr/bin/env python3
"""
cat_3d_concept.py — Generate a low-poly 3D chibi cat mesh (.obj + .mtl)
matching the Catppuccin palette from the copilot-cat SVG sprites.

Usage:
    pip install numpy          # trimesh is optional
    python cat_3d_concept.py   # outputs cat_model.obj + cat_model.mtl

The script uses pure numpy math to generate primitives (spheres, ellipsoids,
cylinders, cones, bezier curves) and writes raw OBJ/MTL data. No trimesh
dependency required — numpy is the only requirement.
"""

import math
import os
import sys

try:
    import numpy as np
except ImportError:
    print("ERROR: numpy is required. Install with: pip install numpy")
    sys.exit(1)

# ---------------------------------------------------------------------------
# Catppuccin palette (matching current SVG sprites)
# ---------------------------------------------------------------------------
COLORS = {
    "body":       (0x7f / 255, 0x84 / 255, 0x9c / 255),  # #7f849c gray
    "outline":    (0x45 / 255, 0x47 / 255, 0x5a / 255),  # #45475a dark
    "belly":      (0xba / 255, 0xc2 / 255, 0xde / 255),  # #bac2de light belly
    "chest":      (0xcd / 255, 0xd6 / 255, 0xf4 / 255),  # #cdd6f4 white chest
    "pink":       (0xf5 / 255, 0xc2 / 255, 0xe7 / 255),  # #f5c2e7 toe beans/inner ear
    "nose":       (0xf3 / 255, 0x8b / 255, 0xa8 / 255),  # #f38ba8 nose
    "eye_blue":   (0x89 / 255, 0xb4 / 255, 0xfa / 255),  # #89b4fa right eye
    "eye_amber":  (0xfa / 255, 0xb3 / 255, 0x87 / 255),  # #fab387 left eye
    "ear_inner":  (0xcd / 255, 0xd6 / 255, 0xf4 / 255),  # #cdd6f4 white inner ear
    "paw":        (0xf5 / 255, 0xc2 / 255, 0xe7 / 255),  # #f5c2e7 paw tips
}


# ---------------------------------------------------------------------------
# Mesh accumulator — collects vertices, normals, faces across multiple parts
# ---------------------------------------------------------------------------
class MeshBuilder:
    def __init__(self):
        self.vertices = []
        self.normals = []
        self.faces = []        # list of (v_indices, material_name)
        self.materials = {}    # name -> (r, g, b)
        self._v_offset = 0

    def add_material(self, name, rgb):
        self.materials[name] = rgb

    def add_mesh(self, verts, faces, material, normals=None):
        """Add a sub-mesh. verts: Nx3, faces: Mx3 (0-indexed), normals: Nx3."""
        base = self._v_offset
        for v in verts:
            self.vertices.append(v)
        if normals is not None:
            for n in normals:
                self.normals.append(n)
        else:
            for v in verts:
                self.normals.append((0, 1, 0))
        for f in faces:
            self.faces.append(([f[0] + base, f[1] + base, f[2] + base], material))
        self._v_offset += len(verts)

    def face_count(self):
        return len(self.faces)

    def write_obj(self, obj_path, mtl_path):
        mtl_basename = os.path.basename(mtl_path)
        with open(obj_path, "w") as fp:
            fp.write(f"# Copilot Cat 3D - low-poly chibi cat\n")
            fp.write(f"# Generated by cat_3d_concept.py\n")
            fp.write(f"# Faces: {len(self.faces)}\n\n")
            fp.write(f"mtllib {mtl_basename}\n\n")

            for v in self.vertices:
                fp.write(f"v {v[0]:.6f} {v[1]:.6f} {v[2]:.6f}\n")
            fp.write("\n")

            for n in self.normals:
                fp.write(f"vn {n[0]:.6f} {n[1]:.6f} {n[2]:.6f}\n")
            fp.write("\n")

            current_mat = None
            for f_indices, mat in self.faces:
                if mat != current_mat:
                    fp.write(f"\nusemtl {mat}\n")
                    current_mat = mat
                i0, i1, i2 = f_indices
                # OBJ is 1-indexed
                fp.write(f"f {i0+1}//{i0+1} {i1+1}//{i1+1} {i2+1}//{i2+1}\n")

        with open(mtl_path, "w") as fp:
            fp.write(f"# Copilot Cat materials - Catppuccin palette\n\n")
            for name, (r, g, b) in self.materials.items():
                fp.write(f"newmtl {name}\n")
                fp.write(f"Ka {r:.4f} {g:.4f} {b:.4f}\n")
                fp.write(f"Kd {r:.4f} {g:.4f} {b:.4f}\n")
                fp.write(f"Ks 0.1000 0.1000 0.1000\n")
                fp.write(f"Ns 32.0\n")
                fp.write(f"d 1.0\n")
                fp.write(f"illum 2\n\n")


# ---------------------------------------------------------------------------
# Primitive generators (pure numpy)
# ---------------------------------------------------------------------------

def make_sphere(radius, center, lat_steps=12, lon_steps=16):
    """Generate a UV-sphere. Returns (vertices, normals, faces)."""
    verts = []
    norms = []
    cx, cy, cz = center

    for i in range(lat_steps + 1):
        theta = math.pi * i / lat_steps
        for j in range(lon_steps):
            phi = 2.0 * math.pi * j / lon_steps
            nx = math.sin(theta) * math.cos(phi)
            ny = math.cos(theta)
            nz = math.sin(theta) * math.sin(phi)
            verts.append((cx + radius * nx, cy + radius * ny, cz + radius * nz))
            norms.append((nx, ny, nz))

    faces = []
    for i in range(lat_steps):
        for j in range(lon_steps):
            curr = i * lon_steps + j
            next_j = i * lon_steps + (j + 1) % lon_steps
            below = (i + 1) * lon_steps + j
            below_next = (i + 1) * lon_steps + (j + 1) % lon_steps
            if i != 0:
                faces.append((curr, below, next_j))
            if i != lat_steps - 1:
                faces.append((next_j, below, below_next))

    return verts, norms, faces


def make_ellipsoid(rx, ry, rz, center, lat_steps=12, lon_steps=16):
    """Generate a UV-sphere scaled to ellipsoid dimensions."""
    verts, norms, faces = make_sphere(1.0, (0, 0, 0), lat_steps, lon_steps)
    cx, cy, cz = center
    scaled_verts = [(cx + v[0]*rx, cy + v[1]*ry, cz + v[2]*rz) for v in verts]
    # Approximate normals for ellipsoid
    scaled_norms = []
    for n in norms:
        nx = n[0] / (rx * rx) if rx else 0
        ny = n[1] / (ry * ry) if ry else 0
        nz = n[2] / (rz * rz) if rz else 0
        length = math.sqrt(nx*nx + ny*ny + nz*nz) or 1.0
        scaled_norms.append((nx/length, ny/length, nz/length))
    return scaled_verts, scaled_norms, faces


def make_cylinder(radius, height, center, segments=12, caps=True):
    """Generate a cylinder along the Y axis centered at `center`."""
    verts = []
    norms = []
    faces = []
    cx, cy, cz = center
    half_h = height / 2

    # Side vertices: two rings
    for ring in range(2):
        y = cy + half_h if ring == 0 else cy - half_h
        for i in range(segments):
            angle = 2 * math.pi * i / segments
            nx = math.cos(angle)
            nz = math.sin(angle)
            verts.append((cx + radius * nx, y, cz + radius * nz))
            norms.append((nx, 0, nz))

    # Side faces
    for i in range(segments):
        top = i
        top_next = (i + 1) % segments
        bot = segments + i
        bot_next = segments + (i + 1) % segments
        faces.append((top, bot, top_next))
        faces.append((top_next, bot, bot_next))

    if caps:
        # Top cap
        top_center = len(verts)
        verts.append((cx, cy + half_h, cz))
        norms.append((0, 1, 0))
        for i in range(segments):
            next_i = (i + 1) % segments
            faces.append((top_center, next_i, i))

        # Bottom cap
        bot_center = len(verts)
        verts.append((cx, cy - half_h, cz))
        norms.append((0, -1, 0))
        for i in range(segments):
            next_i = (i + 1) % segments
            faces.append((bot_center, segments + i, segments + next_i))

    return verts, norms, faces


def make_cone(radius, height, center, segments=12):
    """Generate a cone with apex at top, base at bottom, along Y axis."""
    verts = []
    norms = []
    faces = []
    cx, cy, cz = center

    # Apex
    verts.append((cx, cy + height / 2, cz))
    norms.append((0, 1, 0))

    # Base ring
    slope = radius / height
    for i in range(segments):
        angle = 2 * math.pi * i / segments
        nx = math.cos(angle)
        nz = math.sin(angle)
        verts.append((cx + radius * nx, cy - height / 2, cz + radius * nz))
        ny_norm = slope
        length = math.sqrt(nx*nx + ny_norm*ny_norm + nz*nz) or 1.0
        norms.append((nx/length, ny_norm/length, nz/length))

    # Side faces
    for i in range(segments):
        next_i = (i + 1) % segments
        faces.append((0, i + 1, next_i + 1))

    # Base cap
    base_center = len(verts)
    verts.append((cx, cy - height / 2, cz))
    norms.append((0, -1, 0))
    for i in range(segments):
        next_i = (i + 1) % segments
        faces.append((base_center, next_i + 1, i + 1))

    return verts, norms, faces


def bezier_curve(p0, p1, p2, p3, steps=20):
    """Evaluate a cubic bezier curve, returning `steps+1` points."""
    points = []
    for i in range(steps + 1):
        t = i / steps
        u = 1 - t
        x = u**3*p0[0] + 3*u**2*t*p1[0] + 3*u*t**2*p2[0] + t**3*p3[0]
        y = u**3*p0[1] + 3*u**2*t*p1[1] + 3*u*t**2*p2[1] + t**3*p3[1]
        z = u**3*p0[2] + 3*u**2*t*p1[2] + 3*u*t**2*p2[2] + t**3*p3[2]
        points.append((x, y, z))
    return points


def make_tube_along_path(path_points, radius, segments=8):
    """Sweep a circle along a path to create a tube mesh."""
    verts = []
    norms = []
    faces = []
    n_pts = len(path_points)

    for idx, pt in enumerate(path_points):
        # Compute tangent
        if idx == 0:
            tangent = np.array(path_points[1]) - np.array(path_points[0])
        elif idx == n_pts - 1:
            tangent = np.array(path_points[-1]) - np.array(path_points[-2])
        else:
            tangent = np.array(path_points[idx + 1]) - np.array(path_points[idx - 1])
        tangent = tangent / (np.linalg.norm(tangent) + 1e-9)

        # Build local frame (Frenet-like)
        up = np.array([0, 1, 0])
        if abs(np.dot(tangent, up)) > 0.99:
            up = np.array([1, 0, 0])
        side = np.cross(tangent, up)
        side = side / (np.linalg.norm(side) + 1e-9)
        up2 = np.cross(side, tangent)
        up2 = up2 / (np.linalg.norm(up2) + 1e-9)

        center = np.array(pt)
        for i in range(segments):
            angle = 2 * math.pi * i / segments
            offset = radius * (math.cos(angle) * side + math.sin(angle) * up2)
            v = center + offset
            n = offset / (np.linalg.norm(offset) + 1e-9)
            verts.append(tuple(v))
            norms.append(tuple(n))

    # Faces between rings
    for ring in range(n_pts - 1):
        for i in range(segments):
            curr = ring * segments + i
            next_i = ring * segments + (i + 1) % segments
            below = (ring + 1) * segments + i
            below_next = (ring + 1) * segments + (i + 1) % segments
            faces.append((curr, below, next_i))
            faces.append((next_i, below, below_next))

    return verts, norms, faces


def make_small_sphere(radius, center, steps=6):
    """Low-poly sphere for small features like eyes, nose."""
    return make_sphere(radius, center, lat_steps=steps, lon_steps=steps * 2)


# ---------------------------------------------------------------------------
# Build the cat!
# ---------------------------------------------------------------------------

def build_cat():
    mb = MeshBuilder()

    # Register materials
    mb.add_material("body", COLORS["body"])
    mb.add_material("outline", COLORS["outline"])
    mb.add_material("belly", COLORS["belly"])
    mb.add_material("chest", COLORS["chest"])
    mb.add_material("pink", COLORS["pink"])
    mb.add_material("nose_mat", COLORS["nose"])
    mb.add_material("eye_blue", COLORS["eye_blue"])
    mb.add_material("eye_amber", COLORS["eye_amber"])
    mb.add_material("ear_inner", COLORS["ear_inner"])
    mb.add_material("paw", COLORS["paw"])

    # -- HEAD (large chibi sphere) --
    # SVG: 36x34 ellipse → scale to 3D: rx=1.8, ry=1.7, rz=1.6
    head_center = (0, 3.5, 0)
    v, n, f = make_ellipsoid(1.8, 1.7, 1.6, head_center, 14, 20)
    mb.add_mesh(v, f, "body", n)

    # -- BODY (smaller ellipsoid below head) --
    # SVG: 40x22 ellipse → rx=2.0, ry=1.1, rz=1.4
    body_center = (0, 1.2, 0)
    v, n, f = make_ellipsoid(2.0, 1.3, 1.4, body_center, 12, 18)
    mb.add_mesh(v, f, "body", n)

    # -- BELLY patch (slightly larger ellipsoid clipped to front) --
    belly_center = (0, 1.3, 0.6)
    v, n, f = make_ellipsoid(1.4, 1.0, 0.6, belly_center, 10, 14)
    mb.add_mesh(v, f, "belly", n)

    # -- CHEST patch --
    chest_center = (0, 2.1, 0.8)
    v, n, f = make_ellipsoid(0.8, 0.6, 0.4, chest_center, 8, 12)
    mb.add_mesh(v, f, "chest", n)

    # -- EARS (cones) --
    ear_height = 1.2
    ear_radius = 0.6
    # Left ear
    ear_l_center = (-1.0, 5.2, 0)
    v, n, f = make_cone(ear_radius, ear_height, ear_l_center, 10)
    mb.add_mesh(v, f, "body", n)
    # Left ear inner
    v, n, f = make_cone(ear_radius * 0.55, ear_height * 0.7, 
                         (ear_l_center[0], ear_l_center[1] - 0.1, ear_l_center[2] + 0.15), 8)
    mb.add_mesh(v, f, "ear_inner", n)

    # Right ear
    ear_r_center = (1.0, 5.2, 0)
    v, n, f = make_cone(ear_radius, ear_height, ear_r_center, 10)
    mb.add_mesh(v, f, "body", n)
    # Right ear inner
    v, n, f = make_cone(ear_radius * 0.55, ear_height * 0.7,
                         (ear_r_center[0], ear_r_center[1] - 0.1, ear_r_center[2] + 0.15), 8)
    mb.add_mesh(v, f, "ear_inner", n)

    # -- EYES (heterochromia) --
    eye_y = 3.7
    eye_z = 1.35
    eye_radius = 0.35
    # Outline (dark sphere slightly larger, behind eye)
    v, n, f = make_small_sphere(eye_radius * 1.25, (-0.65, eye_y, eye_z), 6)
    mb.add_mesh(v, f, "outline", n)
    v, n, f = make_small_sphere(eye_radius * 1.25, (0.65, eye_y, eye_z), 6)
    mb.add_mesh(v, f, "outline", n)
    # Left eye — amber (when facing forward; matches "left" in SVG description)
    v, n, f = make_small_sphere(eye_radius, (-0.65, eye_y, eye_z + 0.05), 6)
    mb.add_mesh(v, f, "eye_amber", n)
    # Right eye — blue
    v, n, f = make_small_sphere(eye_radius, (0.65, eye_y, eye_z + 0.05), 6)
    mb.add_mesh(v, f, "eye_blue", n)

    # -- NOSE --
    nose_center = (0, 3.2, 1.55)
    v, n, f = make_small_sphere(0.18, nose_center, 5)
    mb.add_mesh(v, f, "nose_mat", n)

    # -- WHISKER DOTS (tiny spheres on cheeks) --
    for side in [-1, 1]:
        for dy in [-0.1, 0.1]:
            v, n, f = make_small_sphere(0.06, (side * 0.9, 3.15 + dy, 1.4), 4)
            mb.add_mesh(v, f, "outline", n)

    # -- LEGS (4 stubby cylinders with paw tips) --
    leg_radius = 0.4
    leg_height = 1.2
    leg_positions = [
        (-1.0, -0.1, -0.5),   # back-left
        (1.0, -0.1, -0.5),    # back-right
        (-0.8, -0.1, 0.5),    # front-left
        (0.8, -0.1, 0.5),     # front-right
    ]
    for lx, ly, lz in leg_positions:
        v, n, f = make_cylinder(leg_radius, leg_height, (lx, ly, lz), 10, caps=True)
        mb.add_mesh(v, f, "body", n)
        # Paw tip (pink sphere at bottom)
        v, n, f = make_small_sphere(leg_radius * 1.1, (lx, ly - leg_height / 2, lz), 6)
        mb.add_mesh(v, f, "paw", n)
        # Toe beans (3 tiny pink spheres on front of paw)
        for i, angle_off in enumerate([-0.3, 0, 0.3]):
            bean_x = lx + math.sin(angle_off) * leg_radius * 0.6
            bean_z = lz + math.cos(angle_off) * leg_radius * 0.6
            v, n, f = make_small_sphere(0.08, (bean_x, ly - leg_height / 2 - 0.05, bean_z), 4)
            mb.add_mesh(v, f, "pink", n)

    # -- TAIL (bezier tube curving up and back) --
    tail_start = (0, 1.5, -1.4)
    tail_cp1 = (0, 2.5, -2.5)
    tail_cp2 = (0.5, 4.0, -2.8)
    tail_end = (0.3, 4.5, -2.2)
    tail_path = bezier_curve(tail_start, tail_cp1, tail_cp2, tail_end, steps=16)
    # Taper the tail: thicker at base, thinner at tip
    tapered_verts = []
    tapered_norms = []
    tapered_faces = []
    segs = 8
    n_pts = len(tail_path)
    for idx, pt in enumerate(tail_path):
        t = idx / (n_pts - 1)
        r = 0.3 * (1.0 - 0.7 * t)  # taper from 0.3 to 0.09

        if idx == 0:
            tangent = np.array(tail_path[1]) - np.array(tail_path[0])
        elif idx == n_pts - 1:
            tangent = np.array(tail_path[-1]) - np.array(tail_path[-2])
        else:
            tangent = np.array(tail_path[idx + 1]) - np.array(tail_path[idx - 1])
        tangent = tangent / (np.linalg.norm(tangent) + 1e-9)

        up = np.array([0, 1, 0])
        if abs(np.dot(tangent, up)) > 0.99:
            up = np.array([1, 0, 0])
        side = np.cross(tangent, up)
        side = side / (np.linalg.norm(side) + 1e-9)
        up2 = np.cross(side, tangent)
        up2 = up2 / (np.linalg.norm(up2) + 1e-9)

        center = np.array(pt)
        for i in range(segs):
            angle = 2 * math.pi * i / segs
            offset = r * (math.cos(angle) * side + math.sin(angle) * up2)
            v = center + offset
            n = offset / (np.linalg.norm(offset) + 1e-9)
            tapered_verts.append(tuple(v))
            tapered_norms.append(tuple(n))

    for ring in range(n_pts - 1):
        for i in range(segs):
            curr = ring * segs + i
            next_i = ring * segs + (i + 1) % segs
            below = (ring + 1) * segs + i
            below_next = (ring + 1) * segs + (i + 1) % segs
            tapered_faces.append((curr, below, next_i))
            tapered_faces.append((next_i, below, below_next))

    mb.add_mesh(tapered_verts, tapered_faces, "body", tapered_norms)

    # -- HEART MARKING on forehead --
    # Small heart shape approximated with two overlapping spheres + a cone
    heart_y = 4.4
    heart_z = 1.45
    heart_r = 0.15
    v, n, f = make_small_sphere(heart_r, (-0.12, heart_y, heart_z), 5)
    mb.add_mesh(v, f, "pink", n)
    v, n, f = make_small_sphere(heart_r, (0.12, heart_y, heart_z), 5)
    mb.add_mesh(v, f, "pink", n)
    # Heart bottom point
    v, n, f = make_cone(0.18, 0.25, (0, heart_y - 0.2, heart_z), 6)
    mb.add_mesh(v, f, "pink", n)

    return mb


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    obj_path = os.path.join(script_dir, "cat_model.obj")
    mtl_path = os.path.join(script_dir, "cat_model.mtl")

    print("Building 3D chibi cat mesh...")
    mb = build_cat()

    print(f"  Vertices: {len(mb.vertices)}")
    print(f"  Faces:    {mb.face_count()}")
    print(f"  Materials: {len(mb.materials)}")

    if mb.face_count() > 5000:
        print(f"  WARNING: Face count ({mb.face_count()}) exceeds 5000 target.")
        print("  Consider reducing lat/lon steps on primitives.")

    mb.write_obj(obj_path, mtl_path)
    print(f"\nOutput:")
    print(f"  {obj_path}")
    print(f"  {mtl_path}")
    print("\nDone! View with: qml cat_3d_viewer.qml  (requires Qt Quick 3D)")


if __name__ == "__main__":
    main()
